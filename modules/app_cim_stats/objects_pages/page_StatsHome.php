    function classMethod($param1, $param2) 
    {
        // CODE
    }	
    
    
    
    //************************************************************************
	/**
	 * function getHTML
	 * <pre>
	 * This method returns the HTML data generated by this object.
	 * </pre>
	 * @return [STRING] HTML Display data.
	 */
    function getHTML() 
    {

        // Uncomment the following line if you want to create a template 
        // tailored for this page:
        $path = $this->pathModuleRoot.'templates/';
        // Otherwise use the standard Templates for the site:
        //$path = SITE_PATH_TEMPLATES;
        

        /*
         * Update any label tags ...
         */
        // example:
            // $name = $user->getName();
            // $this->labels->setLabelTag( '[Title]', '[userName]', $name);


        // NOTE:  this parent method prepares the $this->template with the 
        // common Display data.  
        $this->prepareTemplate($path);
        
        $this->template->set('coordinatorLinkValues', $this->coordinatorLinks );
        $this->template->set('cdLinkValues', $this->cdLinks );
        $this->template->set('rtLinkValues', $this->rtLinks );
        $this->template->set('ntLinkValues', $this->ntLinks );
        
        $this->template->set('isNational',$this->isNational);
        $this->template->set('isRegional',$this->isRegional);
        $this->template->set('isCD',$this->isCD);
        $this->template->set('isStatsCoordinator',$this->isStatsCoordinator);
        $this->template->set('isAllStaff',$this->isAllStaff);
        
        // uncomment this line if you are creating a template for this page
		$templateName = 'page_StatsHome.php';
		// otherwise use the generic site template
		//$templateName = '';
		
		return $this->template->fetch( $templateName );
        
    }
    
    function setMyLinks( $allStaffLinks, $coordinatorLinks, $cdLinks, $rtLinks, $ntLinks )
    {
        $this->setLinks( $allStaffLinks );
        
        $this->coordinatorLinks = $coordinatorLinks;
        $this->cdLinks = $cdLinks;
        $this->rtLinks = $rtLinks;
        $this->ntLinks = $ntLinks;
        
        return;
    }
    
    function setPerms( $isNational, $isRegional, $isCD, $isStatsCoordinator, $isAllStaff )
    {
        $this->isNational = $isNational;
        $this->isRegional = $isRegional;
        $this->isCD = $isCD;
        $this->isStatsCoordinator = $isStatsCoordinator;
        $this->isAllStaff = $isAllStaff;
        
    }
	
}

