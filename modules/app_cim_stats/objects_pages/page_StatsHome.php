class  page_StatsHome extends PageDisplay {

	//CONSTANTS:
    
    /** The Multilingual Page Key for labels on this page */
    const MULTILINGUAL_PAGE_KEY = 'page_StatsHome';
    

	//VARIABLES:
	
	/** @var [OBJECT] The viewer object. */
	protected $viewer;
	
    /** @var [STRING] The path to this module's root directory. */
	protected $pathModuleRoot;
	
	/** @var [ARRAY] all the links accessible to campus stats coordinators. */
	protected $coordinatorLinks;	
	
	/** @var [ARRAY] all the links accessible to campus directors. */
	protected $cdLinks;

	/** @var [ARRAY] all the links accessible to the regional teams. */
	protected $rtLinks;
	
	/** @var [ARRAY] all the links accessible to the national team. */
	protected $ntLinks;
	
	protected $isNational;
	protected $isRegional;
	protected $isCD;
	protected $isStatsCoordinator;
	protected $isAllStaff;


	//CLASS CONSTRUCTOR
	//************************************************************************
	/**
	 * function __construct
	 * <pre>
	 * Initialize the object.
	 * </pre>
	 * @param $pathModuleRoot [STRING] The path to the module's root dir.
	 * @param $viewer [OBJECT] The viewer object.
	 * @return [void]
	 */
    function __construct($pathModuleRoot, $viewer ) 
    {
    
        parent::__construct();
        
        // initialzie the object values
        $this->pathModuleRoot = $pathModuleRoot;
        $this->viewer = $viewer;
        
        // now initialize the labels for this page
        // start by loading the default field labels for this Module
        $languageID = $viewer->getLanguageID();
        $seriesKey = modulecim_stats::MULTILINGUAL_SERIES_KEY;
        $pageKey = page_StatsHome::MULTILINGUAL_PAGE_KEY;
        $this->labels = new MultilingualManager( $languageID, $seriesKey, $pageKey );
        
        $this->isNational = false;
        $this->isRegional = false;
        $this->isCD = false;
        $this->isStatsCoordinator = false;
        $this->isAllStaff = false;

         
    }



	//CLASS FUNCTIONS:
	//************************************************************************
	/**
	 * function classMethod
