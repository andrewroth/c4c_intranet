<p class="text"><b>Support raising</b> is one of the preparations you will need to do for your summer project.  The summer project tool will let you record how much support you have raised as well as check what has been processed at the C4C HQ.  </p>
 
<p class="text">Here are some key support deadline dates to remember for the 2007 projects: </p>

<span class="bold">International Projects: </span>

<ol>
   <li>$1500 by March 23 for International Projects (rational - covers tickets) </li>
   <li>Additional Support sent in by April 18 (OEX only) </li>
   <li>Remainder of your support sent by April 27th </li>
</ol>

<span class="bold">National Projects: </span>

<ol>
   <li>$1600 taken to Orientation (we strongly recommend this be the minimum & that students aim to have all of the support raised by orientation) </li>
   <li>Remainder submitted by June 1st  </li>
</ol>

<span class="bold">Some key things to keep in mind: </span>
<ol>
   <li>Please do NOT have your parents send a 'deposit' cheque to cover your support. All cheques received are processed as donations and are non-refundable.</li>
   <li>Once your airline ticket is confirmed you are responsible to cover the cost of the ticket even if you are unable to go on the project.</li>
   <li>If you have not raised the full project amount by April 27 (or June 1st for National Projects)  then you are responsible to cover the outstanding amount.</li>
</ol>

<p class="text">Please direct any questions to <a href="mailto:projects@campusforchrist.org">projects@campusforchrist.org</a></p>